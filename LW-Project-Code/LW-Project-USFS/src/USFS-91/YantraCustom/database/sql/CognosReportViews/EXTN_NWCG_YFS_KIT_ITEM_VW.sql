CREATE OR REPLACE FORCE VIEW EXTN_NWCG_YFS_KIT_ITEM_VW ("ITEM_ID", "KIT_ITEM_KEY", "ITEM_KEY", "COMPONENT_ITEM_KEY", "KIT_QUANTITY", "LOCKID", "CREATETS", "MODIFYTS", "CREATEUSERID", "MODIFYUSERID", "CREATEPROGID", "MODIFYPROGID")
AS
  SELECT A.ITEM_ID,
    B.KIT_ITEM_KEY,
    B.ITEM_KEY,
    B.COMPONENT_ITEM_KEY,
    KIT_QUANTITY,
    B.LOCKID,
    B.CREATETS,
    B.MODIFYTS,
    B.CREATEUSERID,
    B.MODIFYUSERID,
    B.CREATEPROGID,
    B.MODIFYPROGID
  FROM yfs_item A,
    yfs_kit_item B
  WHERE A.ITEM_KEY= B.COMPONENT_ITEM_KEY;