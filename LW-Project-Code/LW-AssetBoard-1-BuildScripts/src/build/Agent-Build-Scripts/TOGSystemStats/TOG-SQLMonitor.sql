SELECT 'ELAPSED_TIME_SEC' || ',' || 'ACTIVITY_STATE' || ',' || 'QUERY_COST_ESTIMATE' || ',' || 'STMT_TEXTnewLine' FROM dual;
select ELAPSED_TIME_SEC || ',' || ACTIVITY_STATE || ',' || QUERY_COST_ESTIMATE || ',' || STMT_TEXT || 'newLine'
from SYSIBMADM.MON_CURRENT_SQL 
where ((ELAPSED_TIME_SEC > 900 or (QUERY_COST_ESTIMATE > 100000 and ELAPSED_TIME_SEC > 420)) and ACTIVITY_STATE = 'EXECUTING')
order by ELAPSED_TIME_SEC desc;