CREATE OR REPLACE FORCE VIEW EXTN_NWCG_INCIDENT_OTH_RPT2_VW ("ITEM_ID", "SERIAL_NO", "CLASS", "ITEM_DESCRIPTION", "UOM", "QTY_AS_ITEM", "QTY_AS_KITS", "TRANSFER_IN", "TRANSFER_IN_AS_KITS", "RFBXFER_IN_AS_ITEMS", "RFBXFER_IN_AS_KITS", "RCD_ITEMS", "RCD_KIT_ITEMS", "RCD_IN_KITS", "TRANSFER_OUT", "TRANSFER_OUT_AS_KITS", "RFBXFER_OUT_AS_ITEMS", "RFBXFER_OUT_AS_KITS", "INCIDENT NO", "EXTN_INCIDENT_NAME", "OTHER ACCT CODE", "BLM ACCT CODE", "FS ACCT CODE", "EXTN_OVERRIDE_CODE", "YEAR", "CACHE ID")
AS
  SELECT ITEM_ID,
    SERIAL_NO,
    CLASS,
    ITEM_DESCRIPTION,
    UOM,
    SUM(QTY_AS_ITEM),
    SUM(QTY_AS_KITS),
    SUM(TRANSFER_IN),
    SUM(TRANSFER_IN_AS_KITS),
    SUM(RFBXFER_IN_AS_ITEMS),
    SUM(RFBXFER_IN_AS_KITS),
    SUM(RCD_ITEMS),
    SUM(RCD_KIT_ITEMS),
    SUM(RCD_IN_KITS),
    SUM(TRANSFER_OUT),
    SUM(TRANSFER_OUT_AS_KITS),
    SUM(RFBXFER_OUT_AS_ITEMS),
    SUM(RFBXFER_OUT_AS_KITS),
    "INCIDENT NO",
    EXTN_INCIDENT_NAME,
    "OTHER ACCT CODE",
    "BLM ACCT CODE",
    "FS ACCT CODE",
    EXTN_OVERRIDE_CODE,
    "YEAR",
    "CACHE ID"
  FROM EXTN_NWCG_INCIDENT_OTH_RPTS_VW
  GROUP BY ITEM_ID,
    SERIAL_NO,
    CLASS,
    ITEM_DESCRIPTION,
    UOM,
    "INCIDENT NO",
    EXTN_INCIDENT_NAME,
    "OTHER ACCT CODE",
    "BLM ACCT CODE",
    "FS ACCT CODE",
    EXTN_OVERRIDE_CODE,
    "YEAR",
    "CACHE ID";